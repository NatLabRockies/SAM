"""
Identify bad modules from test results.

Store bad modules in CSV file.

Make clean library file by removing bad files from library file created by SixParSolve.py.
"""

import pandas as pd
from pathlib import Path

# Get list of modules and their performance ratios
df_all = pd.read_csv("cec_modules_test_2026-7-3_2026-Jun-25.csv", na_values="<null>")

df_all = df_all.set_index("Module Name")

# Identify bad modules and save list to CSV
df_bad = df_all[(df_all["Performance Ratio"].lt(0.655) | df_all["Performance Ratio"].gt(0.82))]

df_bad.to_csv("bad_modules_2026-6-23.csv")

# Remove bad modules from library generated by SixParSolve.py
df_lib_all = pd.read_csv("CEC Modules PV_Module_List_Full_Data_ADA_2026-6-23.csv", index_col="Name")
df_lib_good = df_lib_all.drop(df_bad.index, axis=0)

# Write clean library to CSV
df_lib_good.to_csv("CEC Modules Clean 2026-6-23.csv")
